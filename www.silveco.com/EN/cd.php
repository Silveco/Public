                                <h2>Technical guidelines for the CD/DVD content preparation</h2>
                                <p class="lead">
                                    The content of the replicated CD media is equal to the master disc content, which is why it is very important that master disc is made according to the received CD/DVD standards. In case you do not take into account this rule, it is possible the replicated disc would not work correctly or it will not work properly while using certain CD/DVD players.
                                </p>
                                <hr>
                                <h3>Content preparation for CD/DVD disc replication</h3>
                                <p class="lead">
                                    Please prepare the content for the CD/DVD replication in the following formats:
                                </p>
                                <ul class="smaller">
                                    <li>DDP file</li>
                                    <li>CD-R/DVD-R master</li>
                                </ul>
                                <p class="lead">Supplied CD-R or DVD-R masters must be previously reviewed by the client, for the content on the replicated versions are equal to the one on the original disc. In order to flawlessly manufacture a working master (glass master and stamper), the supplied master must not hold any mechanical damages, fingerprints or scratches.</p>
                                <hr>
                                <h3>Content preparation for CD-ROM discs</h3>
                                <p class="lead">
                                    When preparing master for CD-R discs, we encourage you to check the buffer coefficient of the CD-R disc (minimally 70%). In case this is not possible, check the readability of the CD-R master on different disc players. We recommend that you deliver two masters, each made on different CD/DVD media device.
                                    <br /><br />
                                    CD-R disc should be made according to the Yellow Book standard (standard, which denotes the way of arranging the written data on disc).  Most commercial CD/DVD recording software use Yellow book standard. We do not recommend using sibilants in file names, for sometimes these discs do not work properly.
                                    <br /><br />
                                    We encourage you to test each CD-R disc on different computers and if possible on different operating systems.
                                </p>
                                <hr>

                                <h3>Content preparation for CD-Audio discs</h3>
                                <p class="lead">

                                    Master for CD-Audio disc must entirely meet Red Book CD standard demands. If a client sends a written request to manufacture master under different conditions, Sliveco Ltd does not hold responsibility for any possible irregularities, which might occur during the transmission from the master disc (CD-R) to the glass master.
                                    <br /><br />
                                    1.) Maximum length for audio is 79 minutes.<br />
                                    2.) Maximum headroom is 0 dB, minimum -2 dB.
                                </p>

