                                <h2>Prepress review for CD/DVD disc screen printing</h2>
                                <p class="lead">We recommend screen printing when using large monochromatic or two-coloured for CD/DVD disc artwork layout (e.g. only text and background). Colour layering is thicker than in the offset printing, thus offering stronger contrasts between different colours, which is warmly welcomed in some design cases.</p>
