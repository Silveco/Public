                                <h3>Prépresse pour CD/DVD imprimerie offset</h3>
                                <p class="lead">Nous recommandons l’imprimerie offset pour des disques CD/DVD qui sont composés d’un nombre important de couleurs (p.ex. photos). Nous proposons la technologie offset dans quatre couleurs de base (CMJN).  En plus, il est aussi possible d’ajouter la couleur blanche avant l’impression. Le fond blanc est imprimé en utilisant la flexographie et comprend une surface imprimante de 177 mm de largeur.</p>
                                <p class="lead smaller">Avant la commande, n’oubliez pas de mentionner si le fond blanc est nécessaire. En raison de l’application plus fine des couleurs, les couleurs ne sont pas aussi denses que sur la pochette et en considérant que le disque DVD ou CD est en principe gris,  les nuances peuvent être un peu plus sombres que lorsqu’elles sont imprimés sur le papier ! Nous déconseillons d’utiliser le même design pour l’impression sur le CD/DVD que sur sa pochette.</p>
                                <p class="lead smaller">Toutes les images pour le design doivent être converties en espace de couleur CMJN !</p>
