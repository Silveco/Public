                                <h2>Indications techniques pour la préparation du contenu de CD/DVD</h2>
                                <p class="lead">
                                    Le contenu des médias de CD dupliqués est identique au contenu du disque master, c’est pourquoi il est très important que le disque master soit fabriqué conformément aux standards. Au cas où vous n’auriez pas pris en compte cette règle, il est possible que le disque dupliqué ne puisse  fonctionner correctement  en utilisant certains lecteurs CD/DVD.
                                </p>
                                <hr>
                                <h3>Préparation du contenu pour la duplication de disque CD/DVD</h3>
                                <p class="lead">
                                    Veuillez préparer le contenu pour la duplication de CD/DVD en respectant les formats ci-dessous:
                                </p>
                                <ul class="smaller">
                                    <li>Fichier DDP</li>
                                    <li>CD-R/DVD-R master</li>
                                </ul>
                                <p class="lead">Des masters  CD-R ou DVD-R fournis doivent être  examinés par le client afin de s’assurer que le contenu du disque original et de la version dupliquée sont identiques.  Afin de fabriquer parfaitement la matrice (glass master et matrice de pressage) le master fourni ne doit contenir aucun dommage mécanique, empreintes ou rayures.</p>
                                <hr>
                                <h3>Préparation du contenu pour CD-Rom</h3>
                                <p class="lead">
                                    Lors de la préparation du master CD-R, nous vous invitons à  vérifier le coefficient tampon  du disque CD-R (minimum 70%). Au cas où cela ne serait pas possible, vérifiez la lisibilité du master CD-R sur différents lecteurs.  Nous vous recommandons de livrer deux masters, chacun fabriqué sur un appareil média différent.
                                    <br /><br />
                                    Le disque CD-R doit être fabriqué conformément aux standards du Livre Jaune (standards indiquant la disposition de données écrites sur le disque). La plupart des logiciels d’enregistrement de CD/DVD commerciaux utilisent les standards du Livre Jaune. Nous vous déconseillons d’utiliser des consonnes sibilantes pour les noms de fichiers car il peut arriver que ces disques ne fonctionnent pas correctement.
                                    <br /><br />
                                    Nous vous invitons à tester chaque disque CD-R sur les différents ordinateurs et, si possible, sur les différents systèmes d’exploitation.
                                </p>
                                <hr>

                                <h3>Préparation du contenu des disques CD-Audio</h3>
                                <p class="lead">
                                    Le master pour disque CD-Audio doit entièrement respecter les Standards pour CD du Livre Rouge. Si le client envoie la demande écrite pour la fabrication sous des conditions différentes, Sliveco Ltd n’assume aucune responsabilité  pour toutes les  irrégularités possibles qui pourront apparaître lors de la transmission de disque master (CD-R) au glass master.
