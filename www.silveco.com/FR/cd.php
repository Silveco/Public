                                <h2>Préparation de prépresse pour CD/DVD sérigraphie</h2>
                                <p class="lead">Nous recommandons la sérigraphie  lorsque vous utilisez le design monochromatique ou de deux couleurs pour le disque CD/DVD (p.ex. seulement le texte et le fond). L’application des couleurs est plus épaisse que pour la technologie offset, par conséquent cela offre des contrastes plus forts entre les couleurs ce qui est recommandé vivement dans certains cas de design.</p>
                                <p class="lead smaller">Nous proposons la technologie sérigraphie pour les disques CD/DVD en cinq couleurs et vous avez la possibilité de sélectionner votre couleur parmi celles que le nuancier de couleur Pantone Coated propose.   Nous utilisons des couleurs brillantes pour l’impression, mais , si nécessaire, une impression sur des CD/DVD avec le verni mate est aussi à votre disposition.</p>
                                <p class="lead smaller">La couleur noir soutenu (rich black) n’est pas utilisée lors de la sérigraphie.</p>
                                <p class="lead smaller">Veuillez préciser clairement quelles couleurs vous avez choisi pour votre conception graphique (design).</p>
                                <p class="lead smaller">En raison d’application plus épaisse de la couleur, vous devez être très attentif lorsque vous choisissez la taille et la largeur des lettres. Si les lettres ou les lignes sont trop petites, un remplissage excessif peut se produire dans l’espace destiné aux lettres ou aux lignes.</p>
